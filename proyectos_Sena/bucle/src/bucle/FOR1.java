
package bucle;
import javax.swing.*;
public class FOR1 {
    public static void main(String[] args) {
        System.out.println("\n");
        for (double i = 0; i <= 10; i=i+0.5) {
            System.out.print(i);
        }//imprimir los numero del 20 al 0
        System.out.println("\n");
        for (int x = 20; x >= 0; x--) {
            System.out.print(x);
        }
        System.out.println("\n");
        for (int m = 2; m <=30; m=m+2) {
            System.out.print(m);
        }
        System.out.println("\n");
        for (int i = 60; i >=0; i-=3) {
            System.out.print(i);
        }
    }
}
