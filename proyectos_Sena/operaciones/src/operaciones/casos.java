
package operaciones;

public class casos {
    public static void main(String args[]){
      int a = 6;
      int b = 3;
      char op = '-';
      System.out.print("El resultado es : ");
      if ( op == '+' ) {
          System.out.println( a + b);
      } else if ( op == '-') {
          System.out.println( a - b);
      } else if ( op == '*') {
          System.out.println( a * b);
      } else if ( op == '/') {
          System.out.println( a / b);
      }
   }
    
}
